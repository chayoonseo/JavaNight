package lesson1;

public class Optest {
	public static void main(String[] args) {
		int a = 10;
		int b = 3;

		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / (double)b);
		System.out.println(a % b);

		System.out.println((a + b) / 2);

	}
}
